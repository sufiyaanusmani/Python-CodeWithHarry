# Operators:
#     Arithmetic
print(5 + 6)
print(5 - 6)
print(5 * 6)
print(5 / 6)
print(5 // 6)
print(5 ** 6)
print(5 % 6)

#     Assignment
x = 5
x += 7
x -= 7
x *= 7
x /= 7
x %= 7

#     Comparison
i = 8
print(i == 5)
print(i != 5)
print(i >= 5)
print(i <= 5)
print(i > 5)
print(i < 5)

#     Logical
a = True
b = False
print(a and a)
print(a and b)
print(a or b)

#     Identity
print(a is b)
print(a is not b)

#     Membership
list1 = [3, 3, 2, 2, 33, 35, 32]
print(32 in list1)
print(32 not in list1)

#     Bitwise
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11
print(0 & 1)
print(0 | 1)
